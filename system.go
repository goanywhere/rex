package web

import (
	"os"
)

func Exists(path string) bool {
	if _, err := os.Stat(path); err != nil {
		if os.IsNotExist(err) {
			return false
		}
	}
	return true
}

func IsFile(path string) bool {
	info, err := os.Stat(path)
	if os.IsExist(err) {
		return false
	}
	return !info.IsDir()
}
