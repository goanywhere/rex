package middleware

import (
	"log"
	"net/http"
	"os"
)

var logger = log.New(os.Stdout, "[web.go]", 0)

// ---------------------------------------------------------------------------
//  Logging Middleware Supports
// ---------------------------------------------------------------------------
func Logger(next http.Handler) http.Handler {
	fn := func(w http.ResponseWriter, r *http.Request) {
		next.ServeHTTP(w, r)
	}
	return http.HandlerFunc(fn)
}
